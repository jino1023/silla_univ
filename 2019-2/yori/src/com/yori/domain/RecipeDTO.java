package com.yori.domain;

public class RecipeDTO {
	
	private Integer recNum;
	private String recName;
	private String recText;
	private String recImage;
	private Integer recStar;
	private String cateHow;
	private String cateKind;	
	private String recOrder;
	private String memId;
	
	public Integer getRecNum() {
		return recNum;
	}
	public void setRecNum(Integer recNum) {
		this.recNum = recNum;
	}
	public String getRecName() {
		return recName;
	}
	public void setRecName(String recName) {
		this.recName = recName;
	}
	public String getRecText() {
		return recText;
	}
	public void setRecText(String recText) {
		this.recText = recText;
	}
	public String getRecImage() {
		return recImage;
	}
	public void setRecImage(String recImage) {
		this.recImage = recImage;
	}
	public Integer getRecStar() {
		return recStar;
	}
	public void setRecStar(Integer recStar) {
		this.recStar = recStar;
	}
	public String getCateHow() {
		return cateHow;
	}
	public void setCateHow(String cateHow) {
		this.cateHow = cateHow;
	}
	public String getCateKind() {
		return cateKind;
	}
	public void setCateKind(String cateKind) {
		this.cateKind = cateKind;
	}
	public String getRecOrder() {
		return recOrder;
	}
	public void setRecOrder(String recOrder) {
		this.recOrder = recOrder;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	
	
	
}
